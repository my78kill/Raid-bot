    print(f"рҹ“Ё Processing message from {user_id}: {text}")
    
    # Check authorization
    if not check_authorized(user_id):
        print(f"вқҢ User {user_id} not authorized")
        return
    
    # Auto-group access
    if update.effective_chat.type in [ChatType.GROUP, ChatType.SUPERGROUP]:
        if str(chat_id) not in authorized_groups:
            add_group_access(
                chat_id,
                update.effective_chat.title or f"Group {chat_id}",
                user_id
            )
            print(f"вң… Auto-added group {chat_id}")
    
    # ========== HANDLE .RAID COMMAND ==========
    if text.startswith('.raid'):
        print(f"рҹҺҜ RAID command detected: {text}")
        
        if not update.message.reply_to_message:
            await update.message.reply_text("рҹ“Ң Reply to a user first!\nUsage: .raid 100")
            return
        
        # Parse count
        parts = text.split()
        if len(parts) < 2:
            count = 100
        else:
            try:
                count = int(parts[1])
                count = min(count, MAX_RAID_COUNT)
                count = max(1, count)  # Ensure at least 1
            except ValueError:
                await update.message.reply_text("вқҢ Invalid number! Use: .raid 100")
                return
        
